function toUrlPath(rawPath) {
  return rawPath;
}

function valuesFrom(row, columns) {
  return columns.map((column) => row[column]);
}

async function createTables() {
  for (const statement of schemaStatements) {
    await pool.query(statement);
  }
}

async function insertIfMissing(table, conflictColumn, row) {
  const columns = Object.keys(row);
  const placeholders = columns.map((_, index) => `$${index + 1}`).join(', ');
  const values = valuesFrom(row, columns);
  await pool.query(
    `INSERT INTO ${table} (${columns.join(', ')}) VALUES (${placeholders}) ON CONFLICT (${conflictColumn}) DO NOTHING`,
    values
  );
}

