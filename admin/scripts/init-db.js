async function seedAdminUser() {
  const { rows: countRows } = await pool.query('SELECT COUNT(*)::int AS count FROM admin_users');
  if (countRows[0].count > 0) return;

  const email = process.env.ADMIN_BOOTSTRAP_EMAIL?.trim();
  const password = process.env.ADMIN_BOOTSTRAP_PASSWORD;

  if (!email || !password) {
    console.warn('No admin user exists yet. Set ADMIN_BOOTSTRAP_EMAIL and ADMIN_BOOTSTRAP_PASSWORD before first deploy, or create an admin user manually.');
    return;
  }

  const passwordHash = await bcrypt.hash(password, 12);
  await insertIfMissing('admin_users', 'email', {
    name: process.env.ADMIN_BOOTSTRAP_NAME || 'YPP Admin',
    email,
    password_hash: passwordHash,
  });
}

async function seedIfEmpty(table, rows) {
  if (!seedDefaults) return;
  const { rows: countRows } = await pool.query(`SELECT COUNT(*)::int AS count FROM ${table}`);
  if (countRows[0].count > 0) return;

  for (const row of rows) {
    const columns = Object.keys(row);
    const placeholders = columns.map((_, index) => `$${index + 1}`).join(', ');
    const values = valuesFrom(row, columns);
    await pool.query(`INSERT INTO ${table} (${columns.join(', ')}) VALUES (${placeholders})`, values);
  }
}

